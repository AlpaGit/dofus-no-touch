export default class Box{
    public x0: number = 0;
    public y0: number = 0;
    public x1: number = 0;
    public y1: number = 0;
    public x2: number = 0;
    public y2: number = 0;
    public x3: number = 0;
    public y3: number = 0;

    constructor(x0: number, y0: number, x1: number, y1: number, x2: number, y2: number, x3: number, y3: number){
        this.x0 = x0;
        this.y0 = y0;
        this.x1 = x1;
        this.y1 = y1;
        this.x2 = x2;
        this.y2 = y2;
        this.x3 = x3;
        this.y3 = y3;
    }
}